#include "ft_printf.h"

void	set_base(char *opt, int *plen)
{
	if (opt[4] == 'x' || opt[4] == 'X' || opt[4] == 'p')
		plen[2] = 16;
	else if (opt[4] == 'o' || opt[4] == 'O')
		plen[2] = 8;
	else
		plen[2] = 10;
	if (opt[4] == '%')
		plen[1] = -1;
}
